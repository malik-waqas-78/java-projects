
package com.exam.exam2;


public class Screen {
    
    private String resolution;
    private String type;

    public Screen(String resolution, String type) {
        this.resolution = resolution;
        this.type = type;
    }

    public String getResolution() {
        return resolution;
    }

    public String getType() {
        return type;
    }
    
    
    
}
